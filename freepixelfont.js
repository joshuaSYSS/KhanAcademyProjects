function pixel_text(text, x, y, font_size, colour){
    text = text.toUpperCase();
    var curX = x;
    noStroke();
    fill(colour);
    for(var i = 0;i < text.length;i++){
        if(text[i].charCodeAt(0) >= 'A'.charCodeAt(0) && text[i].charCodeAt(0) <= 'Z'.charCodeAt(0)){
            var idx = text[i].charCodeAt(0) - 'A'.charCodeAt(0);
            for(var x2 = 0; x2 < 4;x2++){
                for(var y2 = 0;y2 < 5;y2++){
                    if(text_design[idx][y2][x2] === 1){
                        rect(curX + x2 * font_size, y + y2 * font_size, font_size, font_size);
                    }
                }
            }
        }
        else if(text[i].charCodeAt(0) >= '!'.charCodeAt(0) && text[i].charCodeAt(0) <= '?'.charCodeAt(0)){
            var idx = text[i].charCodeAt(0) - '!'.charCodeAt(0);
            for(var x2 = 0; x2 < 4;x2++){
                for(var y2 = 0;y2 < 5;y2++){
                    if(symbol_design[idx][y2][x2] === 1){
                        rect(curX + x2 * font_size, y + y2 * font_size, font_size, font_size);
                    }
                }
            }
        }
        else{
            for(var x2 = 0; x2 < 4;x2++){
                for(var y2 = 0;y2 < 5;y2++){
                    if(error_design[y2][x2] === 1){
                        rect(curX + x2 * font_size, y + y2 * font_size, font_size, font_size);
                    }
                }
            }
        }
        curX += font_size * 5;
    }
}
